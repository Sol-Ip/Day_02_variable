package com.kh;

public class Exercise_01 {
	// 각 자료형별 변수를 만들고 초기화한 후 그 값을
	// 출력하는 코드를 작성할 것
	// 단, 변수를 이용하여 출력하시오.
	public static void main(String [] args) {
		int aNum = 100;
		long bNum = 999900000000L;
		float cNum = 486.520F;
		double dNum = 567.890123;
		
		char ch = 'A';
		String str = "Hello World";
		boolean bool = true;
		
		System.out.println("정수 :" + aNum);
		System.out.println("정수(long) : " + bNum);
		System.out.println("실수(float) : " + cNum);
		System.out.println("실수 : " + dNum);
		System.out.println("문자 : " + ch);
		System.out.println("문자열 : " + str);
		System.out.println("논리 : " + bool);
		
		//ctrl + shift + f : 들여쓰기, 정리
		//ctrl + i : 들여쓰기
		//shift + tab : 뒤로 띄어쓰기
	 }

}
	
	


